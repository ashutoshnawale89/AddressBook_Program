package com.AddressBook_program;

public class DataCollect {
String firstName;
String lastName;
String mobileNo;
String city;
String state;
String zip;
String email;
public DataCollect(String firstName, String lastName, String mobileNo, String city, String state, String zip,
		String email) {
	super();
	this.firstName = firstName;
	this.lastName = lastName;
	this.mobileNo = mobileNo;
	this.city = city;
	this.state = state;
	this.zip = zip;
	this.email = email;
}

}
